    public Mostrar_Registros() {
        initComponents();
        this.setIconImage(new ImageIcon(getClass().getResource("/Imagenes/logo.png")).getImage());
        this.setLocationRelativeTo(null);
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jtProductos.setModel(modelo);

            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion conn = new Conexion();
            Connection con = conn.Conectar();

            String sql = "SELECT Codigo_prod, Name_prod, descripcion_prod, id_proveedorProd, Stock_prod, precio_coste_prod"
                    + ", precio_venta_prod, State_Prod FROM productos";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            modelo.addColumn("Codigo");
            modelo.addColumn("Nombre Producto");
            modelo.addColumn("Descripcion");
            modelo.addColumn("Proveedor");
            modelo.addColumn("Stock");
            modelo.addColumn("Precio Coste");
            modelo.addColumn("Precio Venta");
            modelo.addColumn("Estado");

            int[] anchos = {50, 200, 200, 50, 50, 50, 50, 50};
            for (int i = 0; i < jtProductos.getColumnCount(); i++) {
                jtProductos.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }

            while (rs.next()) {
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }

        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }
    }

